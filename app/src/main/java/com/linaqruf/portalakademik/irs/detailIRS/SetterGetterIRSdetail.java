package com.linaqruf.portalakademik.irs.detailIRS;

public class SetterGetterIRSdetail {
    String mingguKe;
    String detail;

    public SetterGetterIRSdetail(String mingguKe, String detail) {
        this.mingguKe = mingguKe;
        this.detail = detail;
    }

    public String getMingguKe() {
        return mingguKe;
    }

    public void setMingguKe(String mingguKe) {
        this.mingguKe = mingguKe;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
}
